package com.exercise.funciones.exercisespoo;

public class SmartDevice {

    double pantalla ;
    int ram;
    double almacenamiento;
    String color;
    String modelo;
    String marca;


    public SmartDevice(){

    }

    public SmartDevice(double pantalla,double almacenamiento,int ram,String color,String modelo,String marca){
        this.pantalla = pantalla;
        this.almacenamiento = almacenamiento;
        this.ram = ram;
        this.color = color;
        this.modelo = modelo;
        this.marca = marca;

    }

}
